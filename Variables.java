class Variables {

    // String - stores text
    // int - stores integer
    // float - stores decimal
    // char - stores character
    // boolean - true or false

    public static void main(String args[]) {

        String myName = "Wasik Ahmed";
        int age = 22;
        float cgpa = 12.5f;
        char sex = 'M';
        boolean isTall = true;

        // print variables
        System.out.println("My name is " + myName);
        System.out.println("My age: " + age);
        System.out.println("My cgpa: " + cgpa);
        System.out.println("Sex: " + sex);
        System.out.println("Tall: " + isTall);

    }
}